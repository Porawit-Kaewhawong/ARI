public class GameStats
{
    // ================================
    // score เป็นข้อมูลภายในของคลาส
    // ประกาศเป็น private → ป้องกันการแก้ค่าจากภายนอก
    // นี่คือหลัก OOP: Encapsulation (การห่อหุ้มข้อมูล)
    // ================================
    private int score;

    // ===============================================
    // AddScore: เมธอด public สำหรับเพิ่มคะแนน
    // เป็นวิธีควบคุมการแก้ข้อมูลแบบปลอดภัย
    // ภายนอกต้องใช้ฟังก์ชันนี้แทนการแก้ score ตรงๆ
    // → Encapsulation
    // ===============================================
    public void AddScore(int amount)
    {
        score += amount;
    }

    // ==========================================================
    // Property: Score
    // get → อ่านค่า score ได้จากภายนอก
    // private set → ภายนอกเขียนค่า score ไม่ได้, เขียนได้เฉพาะภายในคลาส
    // ช่วยควบคุมข้อมูลไม่ให้ถูกแก้ไขมั่ว
    // → Encapsulation (ซ่อนข้อมูล + เปิดเฉพาะสิ่งที่จำเป็น)
    // ==========================================================
    public int Score
    {
        get { return score; }          // ให้คนอื่นอ่านค่าได้

        private set { score = value; } // แต่เซ็ตค่าได้เฉพาะในคลาสนี้เท่านั้น
    }
}